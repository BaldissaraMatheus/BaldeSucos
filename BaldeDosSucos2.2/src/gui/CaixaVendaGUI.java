    private void jButtonExtrasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExtrasActionPerformed
        if ((jTextFieldQtdExtra.getText().isEmpty()) || (jComboBoxExtras.getSelectedIndex() == 0)) {
            JOptionPane.showMessageDialog(null, "Os campos nÃ£o podem retornar vazios!");
        } else {
            jTableVenda.setValueAt(j + 1, j, 0);
            jTableVenda.setValueAt(jComboBoxExtras.getSelectedItem(), j, 1);
            jTableVenda.setValueAt("-", j, 2);
            jTableVenda.setValueAt(jComboBoxExtras.getSelectedItem(), j, 3);
            jTableVenda.setValueAt("-", j, 4);
            jTableVenda.setValueAt("-", j, 5);
            jTableVenda.setValueAt(jTextFieldQtdExtra.getText(), j, 6);
            jTableVenda.setValueAt(jTextFieldValorUnitExtra.getText(), j, 7);
            float valorUnitExtra = Float.parseFloat(jTextFieldValorUnitExtra.getText());
            int qtdExtras = Integer.parseInt(jTextFieldQtdExtra.getText());
            String valorTotExtra = String.valueOf(valorUnitExtra * qtdExtras);
            jTableVenda.setValueAt(valorTotExtra, j, 8);
            j++;
            float desconto = 0;
            if (jFormattedTextFieldDesconto.getText().length() != 0) {
                desconto = Float.parseFloat(jFormattedTextFieldDesconto.getText());
            }
            float descontoTotal = Float.parseFloat(jTextFieldDescontoTotal.getText()) + desconto;
            jTextFieldDescontoTotal.setText(String.valueOf(descontoTotal));
            jFormattedTextFieldDesconto.setText("0.0");
            valorTotalVenda = valorTotalVenda + Float.parseFloat(valorTotExtra) - desconto;
            String valorTotalVenda2 = String.valueOf(valorTotalVenda);
            jTextFieldValorTot.setText(valorTotalVenda2);
            valorUnitExtra = 0;
            valorTotExtra = "0";
            if (jComboBoxExtras.getSelectedItem().equals("Biscoito")) {
                NBiscoito = NBiscoito + qtdExtras;
            } else {
                NBatata = NBatata + qtdExtras;
            }
            qtdExtras = 0;
            jTextFieldTotalItens.setText(String.valueOf(j));
        }

    }//GEN-LAST:event_jButtonExtrasActionPerformed

