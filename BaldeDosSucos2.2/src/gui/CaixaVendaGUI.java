            jTableVenda.setValueAt(jTextFieldQtdSuco.getText(), j, 6);
            jTableVenda.setValueAt(jTextFieldValorUnitSuco.getText(), j, 7);
            float valorUnit = Float.parseFloat(jTextFieldValorUnitSuco.getText());
